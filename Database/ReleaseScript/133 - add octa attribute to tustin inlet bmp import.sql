select toa.TreatmentBMPID, tcat.TreatmentBMPTypeCustomAttributeTypeID, toa.TreatmentBMPTypeID, @OCTA
 from dbo.TustinOctaTemp toa inner join dbo.TreatmentBMPTypeCustomAttributeType tcat on toa.TreatmentBMPTypeID = tcat.TreatmentBMPTypeID
where tcat.CustomAttributeTypeID = @OCTA and toa.OCTA_M2_Funded_ is not null and toa.OCTA_M2_Funded_ <> ''
	--and toa.TreatmentBMPID not in (select TreatmentBMPID from TreatmentBMPTypeCustomAttributeType where TreatmentBMPTypeCustomAttributeTypeID = tcat.TreatmentBMPTypeCustomAttributeTypeID)

