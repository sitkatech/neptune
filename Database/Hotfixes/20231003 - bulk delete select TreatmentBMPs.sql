	select FieldVisitID from #fieldVisitIDsToDelete
)

delete from dbo.FieldVisit where TreatmentBMPID in (
	select TreatmentBMPID from #treatmentBMPIDsToDelete
)

delete from dbo.CustomAttributeValue where CustomAttributeID in (
	select CustomAttributeID from #customAttributeIDsToDelete
