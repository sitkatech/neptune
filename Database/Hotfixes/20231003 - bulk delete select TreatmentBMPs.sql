-- get IDs of existing TreatmentBMPs to delete
select tb.TreatmentBMPID into #treatmentBMPIDsToDelete
from dbo.TreatmentBMP tb
join dbo.TreatmentBMPType tbt on tb.TreatmentBMPTypeID = tbt.TreatmentBMPTypeID
where exists (
	select * from #TreatmentBMPsToDelete tbtd
	where tb.TreatmentBMPName = tbtd.TreatmentBMPName
		and tbt.TreatmentBMPTypeName = tbtd.TreatmentBMPTypeName
)

-- get IDs of related FieldVisits to delete
select FieldVisitID into #fieldVisitIDsToDelete
from dbo.FieldVisit
where TreatmentBMPID in (
	select TreatmentBMPID from #treatmentBMPIDsToDelete
)

-- getIDs of CustomAttributes to delete
select CustomAttributeID into #customAttributeIDsToDelete
from dbo.CustomAttribute
where TreatmentBMPID in (
	select TreatmentBMPID from #treatmentBMPIDsToDelete
)

